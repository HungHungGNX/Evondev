const now = new Date();

// in ra năm
console.log(now.getUTCFullYear());
// in ra tháng
console.log(now.getUTCMonth()); // 0 - 11;
// in ra ngày trong tháng
console.log(now.getUTCDate());
// in ra ngày trong tuần
console.log(now.getUTCDay()); // 0 - 6
// in ra giờ
console.log(now.getUTCHours());
// in ra phút
console.log(now.getUTCMinutes());
// in ra seconds
console.log(now.getUTCSeconds());
// in ra milliseconds
console.log(now.getUTCMilliseconds());

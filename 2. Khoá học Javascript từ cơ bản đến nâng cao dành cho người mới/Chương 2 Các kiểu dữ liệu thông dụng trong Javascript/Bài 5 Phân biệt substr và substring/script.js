const str = "Frontend";

// substr -> Lấy ra 1 phần của chuỗi (string)
// substr(index, length) -> index: vị trí, length: số lượng kí tự bạn muốn lấy
console.log(str.substr(1, 5)); //ronte
// substring -> Lấy ra các kí tự của chuỗi(string)
// substring(start index, end index)
console.log(str.substring(1, 5)); // ront

let students = ["Nam", "Trung", "Hoan", "Long", "Thanh"];

// Hàm tính độ dài của mảng
console.log(students.length);
// Đảo ngược giá trị trong mảng
console.log(students.reverse());
// Nối các phần tử trong mảng thành chuỗi
console.log(students.join());
// Kiểm tra mảng đó có chứa phần tử nào đó không
console.log(students.includes("Nam"));
// Trả về vị trí của phần tử tìm thấy đầu tiên
console.log(students.indexOf("tuan"));
// Trả về vị trí của phần tử tìm thấy cuối cùng
console.log(students.lastIndexOf("nam"));
// Thêm phần tử vào cuối mảng
console.log(students.push("Hung"));
// Thêm phần tử vào đầu mảng
console.log(students.unshift("Tien"));
// Bỏ phần tử cuối khỏi mảng
console.log(students.pop());
// Bỏ phần tử đầu khỏi mảng
console.log(students.unshift());

// == loose equality vs === strict equality
console.log("=== vs == ");
console.log(10 == "10"); // true
console.log(true == 1); // true
console.log(1 == "01"); // true
console.log(null == ""); // false
console.log(10 === "10"); //false;
console.log(10 !== "10"); // true
console.log(true == "true"); // false
